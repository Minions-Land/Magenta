#!/usr/bin/env node

import { readFileSync, writeFileSync } from "node:fs";
import { dirname, resolve } from "node:path";
import { fileURLToPath } from "node:url";
import { MACOS_RELEASE_TRUST_PATH, readMacosReleaseTrust } from "./macos-release-trust.mjs";

const root = resolve(dirname(fileURLToPath(import.meta.url)), "..");
const outputPath = resolve(root, "pi/coding-agent/src/macos-release-trust.generated.ts");
const installerPath = resolve(root, "scripts/install.sh");
const trust = readMacosReleaseTrust(MACOS_RELEASE_TRUST_PATH, { allowUnconfigured: true });
const content = `/**
 * Generated macOS release trust - DO NOT EDIT
 * Generated by scripts/generate-macos-release-trust.mjs
 */

export const MACOS_RELEASE_APPLE_TEAM_ID = ${JSON.stringify(trust.appleTeamId)};
`;
const installerSource = readFileSync(installerPath, "utf8");
const installerTrustPattern = /^EXPECTED_APPLE_TEAM_ID="[^"]*"$/gmu;
const installerTrustMatches = [...installerSource.matchAll(installerTrustPattern)];
if (installerTrustMatches.length !== 1) {
	throw new Error("scripts/install.sh must contain exactly one generated EXPECTED_APPLE_TEAM_ID assignment.");
}
const installerContent = installerSource.replace(
	installerTrustPattern,
	`EXPECTED_APPLE_TEAM_ID=${JSON.stringify(trust.appleTeamId)}`,
);

if (process.argv.length > 3 || (process.argv[2] && process.argv[2] !== "--check")) {
	throw new Error("Usage: generate-macos-release-trust.mjs [--check]");
}
if (process.argv[2] === "--check") {
	const staleOutputs = [];
	if (readFileSync(outputPath, "utf8") !== content) staleOutputs.push("compiled runtime trust");
	if (installerSource !== installerContent) staleOutputs.push("standalone Unix installer trust");
	if (staleOutputs.length > 0) {
		throw new Error(
			`Generated macOS release trust is stale (${staleOutputs.join(", ")}). Run scripts/generate-macos-release-trust.mjs.`,
		);
	}
	process.stdout.write("Generated macOS release trust is current.\n");
} else {
	writeFileSync(outputPath, content);
	if (installerSource !== installerContent) writeFileSync(installerPath, installerContent);
	process.stdout.write(`Generated macOS release trust outputs: ${trust.appleTeamId}\n`);
}
