#!/usr/bin/env node
/**
 * Generate brand version for runtime use.
 * Called during build to inject the active brand's version.
 */

import { readFileSync, writeFileSync } from "fs";
import { join, dirname } from "path";
import { fileURLToPath } from "url";

const __dirname = dirname(fileURLToPath(import.meta.url));
const ROOT = join(__dirname, "..");

// Load active brand
const registryPath = join(ROOT, "brands/registry.toml");
const registrySource = readFileSync(registryPath, "utf-8");
const activeMatch = registrySource.match(/^active = "([^"]+)"/m);
if (!activeMatch) {
	console.error("❌ Could not find active brand in registry.toml");
	process.exit(1);
}

const activeBrand = activeMatch[1];
const brandPathMatch = registrySource.match(
	new RegExp(`\\[\\[brands\\]\\]\\s+name = "${activeBrand}"\\s+path = "([^"]+)"`)
);
if (!brandPathMatch) {
	console.error(`❌ Brand '${activeBrand}' not found in registry.toml`);
	process.exit(1);
}

const brandConfigPath = join(ROOT, "brands", brandPathMatch[1]);
const brandConfigSource = readFileSync(brandConfigPath, "utf-8");

// Extract version
const configMatch = brandConfigSource.match(/export const BRAND_CONFIG[^=]*= (\{[\s\S]+?\n\});/);
if (!configMatch) {
	console.error(`❌ Could not parse BRAND_CONFIG from ${brandConfigPath}`);
	process.exit(1);
}

const BRAND_CONFIG = eval(`(${configMatch[1]})`);

// Generate TypeScript file
const outputPath = join(ROOT, "pi/coding-agent/src/brand-version.generated.ts");
const content = `/**
 * Generated brand version - DO NOT EDIT
 * Generated by scripts/generate-brand-version.mjs
 * Active brand: ${activeBrand}
 */

export const BRAND_VERSION = "${BRAND_CONFIG.version}";
export const BRAND_NAME = "${BRAND_CONFIG.name}";
`;

writeFileSync(outputPath, content);
console.log(`✓ Generated brand version: ${BRAND_CONFIG.name} v${BRAND_CONFIG.version}`);
